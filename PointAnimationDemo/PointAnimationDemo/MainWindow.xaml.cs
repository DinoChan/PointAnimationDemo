    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
            Loaded += OnLoaded;
        }

        private void OnLoaded(object sender, RoutedEventArgs e)
        {
            var storyboard = this.Resources["Storyboard1"] as Storyboard;
            if (storyboard != null)
            {
                storyboard.Begin();
            }
        }
